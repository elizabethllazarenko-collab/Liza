n = int(input("Введіть 5-значне число: "))

d1 = n // 10000          # перша цифра
d2 = (n // 1000) % 10    # друга цифра
d3 = (n // 100) % 10     # третя цифра
d4 = (n // 10) % 10      # четверта цифра
d5 = n % 10              # п'ята цифра

reversed_number = d5 * 10000 + d4 * 1000 + d3 * 100 + d2 * 10 + d1
print(reversed_number)
